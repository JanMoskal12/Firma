#include "czlowiek.h"

//////////////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////////////
czlowiek :: czlowiek(string imie, string nazwisko, string pesel, string email) : _pesel(pesel)
{
  _imie = imie;
  _nazwisko = nazwisko;
  _email = email;
}
//////////////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////////////
string czlowiek :: getImie()
{
    return _imie;
}
//////////////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////////////
string czlowiek :: getNazwisko()
{
    return _nazwisko;
}
//////////////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////////////
string czlowiek :: getPesel()
{
    return _pesel;
}
//////////////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////////////
string czlowiek :: getEmail()
{
    return _email;
}
